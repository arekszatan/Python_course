
# Operatory - znak równości
print("--------------------------")
print("operator ==")
print(f'7 == 8 : {7 == 8}') # False
print(f'10 == 10 : {10 == 10}') # True

print("--------------------------")
print(f'operator !=')
print(f'3 != 41 : {3 != 41}') # True
print(f'5 != 5 : {5 != 5}') # False

print("--------------------------")
print(f'operator <')
print(f'3 < 4 : {3 < 4}') # True
print(f'6 < 6 : {6 < 6}') # False
print(f'4 < 3 : {4 < 3}') # False

print("--------------------------")
print(f'operator >')
print(f'3 > 4 : {3 > 4}') # False
print(f'4 > 4 : {4 > 4}') # False
print(f'4 > 3 : {4 > 3}') # True

print("--------------------------")
print(f'operator >=')
print(f'3 >= 4 : {3 >= 4}') # False
print(f'6 >= 6 : {6 >= 6}') # True
print(f'4 >= 3 : {4 >= 3}') # True

print("--------------------------")
print(f'operator <=')
print(f'3 <= 4 : {3 <= 4}') # True
print(f'6 <= 6 : {6 <= 6}') # True
print(f'4 <= 3 : {4 <= 3}') # False

