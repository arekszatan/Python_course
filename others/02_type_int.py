
# declaration of variables
number1 = 10
number2 = 40
number3 = 50

# addition
result_of_addition = number1 + number2 + number3
print(f'Result of summing number1 , number2 and number3 is {result_of_addition}')

# substraction
result_of_substraction = number3 - number1
print(f'Result of substraction number3 and number1 is {result_of_substraction}')

# multiplication
result_of_multiplication = number1 * number2 * number3
print(f'Result of multiplication number1, number2 and number3 is {result_of_multiplication}')

# division
result_of_division = number3 / number2
print(f'Result of division number3 and number2 is {result_of_division}')

